    <script>
document.addEventListener('DOMContentLoaded', function () {
    const dynamicText = document.querySelector('[data-strings]');
    if (dynamicText) {
        const strings = dynamicText.getAttribute('data-strings').split(', ');
        let stringIndex = 0; // Índice de la palabra actual
        let charIndex = 0;   // Índice del carácter actual
        let isDeleting = false; // Indica si está eliminando caracteres

        const typeEffect = () => {
            const currentString = strings[stringIndex];
            if (isDeleting) {
                // Eliminar un carácter
                charIndex--;
                if (charIndex === 0) {
                    isDeleting = false;
                    stringIndex = (stringIndex + 1) % strings.length; // Cambiar a la siguiente palabra
                }
            } else {
                // Agregar un carácter
                charIndex++;
                if (charIndex === currentString.length) {
                    isDeleting = true; // Empieza a borrar después de completar la palabra
                }
            }

            // Actualizar el texto en el DOM
            dynamicText.textContent = currentString.slice(0, charIndex);

            // Ajustar velocidad
            const typingSpeed = isDeleting ? 100 : 200; // Más rápido al borrar
            setTimeout(typeEffect, typingSpeed);
        };

        // Iniciar el efecto
        typeEffect();
    }
});
</script>
